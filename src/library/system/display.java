    private void nameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nameActionPerformed
        try{
            String sql="select * from Users";
            rs = db.select(sql);
            while(rs.next()){
                String dbname = rs.getString("user_name");
                String dbuser = rs.getString("user_type");
                if(dbname.equals(name.getText()) && dbuser.equals("Employee")){
                name2.setText(rs.getObject(7).toString());
                email2.setText(rs.getObject(2).toString());
                mobile.setText("0"+rs.getObject(3).toString());
                sex.setText(rs.getObject(4).toString());
                age.setText(rs.getObject(5).toString());
                }
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_nameActionPerformed

