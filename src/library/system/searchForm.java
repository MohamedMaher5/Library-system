    private void LoadComboBoxData(JComboBox comboBox)
{
    try 
        {
            String sql = "select book_name from Books order by book_name asc";
            rs = db.select(sql);
            DefaultComboBoxModel  m = new DefaultComboBoxModel();
            
            while (rs.next()) {
                m.addElement(rs.getObject(1).toString());
                }
                comboBox.setModel(m);
            }
        
            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(null, ex.toString());
            }
        
}

