        String s = email.getText();
        String snumber = mobile.getText();
        String sqlInsert = "INSERT INTO Users (user_name,password,email,mobile,sex,age,user_type)\n" +
            "VALUES ('" +name.getText()+"','"+password.getText()+"','"+email.getText()+"','"+mobile.getText()+"','"+sex.getSelectedItem().toString()+"','"+age.getText()+"','Employee');";
        try{
            String pattern = "^[\\w]+@[^\\.]+\\.[\\w]{2,}$";
            String patternnumber = "^(011|012|010)\\d{8}$";
            
            if(!s.matches(pattern))
            JOptionPane.showMessageDialog(rootPane, "Please re-enter your email.");
            
            if(!snumber.matches(patternnumber))
            JOptionPane.showMessageDialog(rootPane, "Please re-enter your mobile number.");
            
            if(s.matches(pattern)&&snumber.matches(patternnumber))
            db.insert(sqlInsert);
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
