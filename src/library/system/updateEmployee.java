    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String s = email2.getText();
        String snumber = mobile.getText();
        String sqlUpdate = "UPDATE Users SET user_name='"+name.getText()+"',password='"+password.getText()+
                "',email='"+email2.getText()+"',mobile='"+mobile.getText()+
                "',sex='"+sex.getSelectedItem().toString()+"',age='"+age.getText()+
                "'WHERE email='"+email.getText()+"';";
        
        String sqlselect = "select * from Users";
        
        try{
            rs = db.select(sqlselect);
            
            while(rs.next()){
                usertype = rs.getString("user_type");
                emailtocheackemployee = rs.getString("email");
                
                if(emailtocheackemployee.equals(email.getText()))
                    if(usertype.equals("Employee")){
                    String pattern = "^[\\w]+@[^\\.]+\\.[\\w]{2,}$";
                    String patternnumber = "^(011|012|010)\\d{8}$";
            
                    if(!s.matches(pattern))
                        JOptionPane.showMessageDialog(rootPane, "Please re-enter your email.");
            
                    if(!snumber.matches(patternnumber))
                        JOptionPane.showMessageDialog(rootPane, "Please re-enter your mobile number.");
                    
                    if(s.matches(pattern) && snumber.matches(patternnumber))
                        db.update(sqlUpdate);
                    }
                    else{
                        JOptionPane.showMessageDialog(rootPane, "This e-mail is not owned by employee.");
                    }
            }
            
        } 
        catch (Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_updateActionPerformed

